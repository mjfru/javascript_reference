/*
! Programming Paradigms
* Essentially, this is how you write and organize your code!

There are 3 main paradigms:

? Object-oriented
  * Organizing data and logic in objects (properties + methods)
  * Organize code in logical entities, think about real-world terms (products, shopping carts, etc.)

? Procedural
  * Write a sequential series of execution steps / tasks
  * "Top to bottom" code execution

? Functional
  * Organize code in (pure) functions with clearly defined tasks
  * Pass data around via parameters
*/

